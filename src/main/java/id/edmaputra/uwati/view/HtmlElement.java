package id.edmaputra.uwati.view;

public class HtmlElement {
	
	private String tabel;
	
	private String navigasiHalaman;
	
	private String grandTotal;

	public String getNavigasiHalaman() {
		return navigasiHalaman;
	}

	public void setNavigasiHalaman(String navigasiHalaman) {
		this.navigasiHalaman = navigasiHalaman;
	}

	public String getTabel() {
		return tabel;
	}

	public void setTabel(String tabel) {
		this.tabel = tabel;
	}

	public String getGrandTotal() {
		return grandTotal;
	}

	public void setGrandTotal(String grandTotal) {
		this.grandTotal = grandTotal;
	}

}
